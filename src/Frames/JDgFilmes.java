    private void btnFilmesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFilmesActionPerformed
        // Listagem dos filmes cadastrados
        
        if(estatico.Dados.getFilmes().isEmpty()){
            areaTexto.append("----------------------------Nenhum filme cadastrado!----------------------------");
        }
        
        // ordenando a lista de filmes
        Collections.sort(estatico.Dados.getFilmes(), (lista1, lista2)-> lista1.getTitulo().compareTo(lista2.getTitulo()));
        
        // criando um formato para a exibição das datas
        DateTimeFormatter formato = DateTimeFormatter.ofPattern("dd/MM/yyyy"); 
        String data = null;
        // limpando o areatTexto
        areaTexto.setText("\n----------------------------Lista de  filmes cadastrados!----------------------------\n");
        //
        estatico.Dados.getFilmes().forEach(((filmes) -> {
            //Fonmatando o resultado do getLancamento da classe Filmes
            String lancamento;
            if(filmes.isLancamento()){
                lancamento = "Sim";
            }else{
                lancamento = "Não";
            }
            
            
            //Exibindo os cadastros para o usuario
            areaTexto.append("Código: " + filmes.getCodigo() + "\t" + "Produtora: " + filmes.getProdutora() + "\n"+
                    "Titulo : "+filmes.getTitulo() + "\t"+"Data LAnçamento: "+ filmes.getAnoLancamento().format(formato) + 
                  "\n"+ "Artista: " + filmes.getArtista() + "\n" + "Genero: " + filmes.getGenero() + "\t" + 
                    "Preço Alocação: " + filmes.getPrecoAluguel() + "\n" + "Duração: " + filmes.getDuracao() + "\t" +
                   "Lançamento: " + lancamento + "\n-----------------------------------------------------------------------------------\n" );
            
        }));
    }//GEN-LAST:event_btnFilmesActionPerformed

