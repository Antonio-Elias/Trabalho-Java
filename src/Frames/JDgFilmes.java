    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        //declaração de variaveil
        String codigo, produtora, artista, titulo;
        double pAlocacao;
        int duracao;
        
        // verificação das entradas de dados
        if(txtCodigo.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Campo Obrigatorio");
            txtCodigo.requestFocus();
            return;
        }else{
            codigo = txtCodigo.getText();
        }
        
        if(txtProdutora.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Campo Obrigatorio");
            txtProdutora.requestFocus();
            return;
        }else{
            produtora = txtProdutora.getText();
        }
        
        if(txtTitulo.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Campo Obrigatorio");
            txtTitulo.requestFocus();
            return;
        }else{
            titulo = txtTitulo.getText();
        }
        
        if(txtArtista.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Campo Obrigatorio");
            txtArtista.requestFocus();
            return;
        }else{
            artista = txtArtista.getText();
        }
        
        if(ftxDataLancamento.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Campo Obrigatorio");
            ftxDataLancamento.requestFocus();
            return;
        }
        
        if(txtPrecoAlocacao.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Campo Obrigatorio");
            txtPrecoAlocacao.requestFocus();
            return;
        }else{
            try{
                pAlocacao = Double.parseDouble(txtPrecoAlocacao.getText());
            }catch(NumberFormatException ex){
                JOptionPane.showMessageDialog(null, "Somente Numeros");
                txtPrecoAlocacao.requestFocus();
                return;
            }
        }
        
       if(txtDuracao.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Campo Obrigatorio");
            txtDuracao.requestFocus();
            return;
        }else{
            try{
                duracao = Integer.parseInt(txtDuracao.getText());
            }catch(NumberFormatException ex){
                JOptionPane.showMessageDialog(null, "Somente Numeros");
                txtDuracao.requestFocus();
                return;
            }
        }
       
       // pegando os valores dos combos
        Genero genero;
        genero = (Genero)cbxGenero.getSelectedItem();
        
        // instanciando uma data
        DateTimeFormatter formato = DateTimeFormatter.ofPattern("dd/MM/yyyy");
        LocalDate data;
        try{
            data = LocalDate.parse(ftxDataLancamento.getText(),formato);
        }catch(DateTimeParseException ex){
            JOptionPane.showMessageDialog(null, "Formato de data Invalida!");
            ftxDataLancamento.requestFocus();
            return;
        }
        
        Filmes filme = new Filmes(codigo, produtora, titulo, artista, data, duracao, 
                genero, pAlocacao, rdbLancamento.isSelected());
        
        estatico.Dados.getFilmes().add(filme);
        
        
        rdbLancamento.setSelected(false);
        txtDuracao.setText("");
        txtProdutora.setText("");
        txtArtista.setText("");
        ftxDataLancamento.setText("");
        txtTitulo.setText("");
        txtPrecoAlocacao.setText("");
        txtCodigo.setText("");
        txtCodigo.requestFocus();
        
        //mensagem
        areaTexto.setText("Filme Adicionado!");
    }//GEN-LAST:event_btnCadastrarActionPerformed

