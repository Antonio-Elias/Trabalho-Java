    private void btnConcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConcluirActionPerformed
       
        
        //datas de aluguel e entrega
        DateTimeFormatter formato = DateTimeFormatter.ofPattern("dd/MM/yyyy");
        LocalDate dataAluguel = LocalDate.now();
        LocalDate dataEntrega = dataAluguel.plusDays(2);
        String dataEn = dataEntrega.format(formato);
        String dataAl = dataAluguel.format(formato);
        
        
        //Valor Total 
        double valorTotal = 0.0d;
        
        valorTotal = filmes.stream().map((filme) -> filme.getPrecoAluguel()).reduce(valorTotal, (accumulator, _item) -> accumulator + _item);
        // status do aluguel
        Status status = Status.END;
        
        // mostrando o restante das informações para o usuario e cliente;
        NumberFormat formatoMoeda = NumberFormat.getCurrencyInstance(new  Locale("pt", "br"));
        areaTexto.append("\n" + "-------Informações de entrega e valor\"-------" +"\n"+ 
                        " Valor total: " + formatoMoeda.format(valorTotal)+ "\n" + "Data Aluguel: "+ dataAl +
                        "\n" +"Data Entega: "+ dataEn   + "\n" + "Status: " + status);
        
        
        Funcionario funcionario = (Funcionario)cbxFuncionario.getSelectedItem();
        Cliente cliente = (Cliente)cbxCliente.getSelectedItem();
        
        btnConcluir.setEnabled(false);
        // criando e addicionando o objeto na lista 
        AluguelFilmes aluguel = new AluguelFilmes(funcionario, cliente, filmes, dataAluguel, dataEntrega, 
                        valorTotal, status);
        Dados.getAluguelfilmes().add(aluguel);
        btnAdicionar.setEnabled(false);
        esvaziaLista();
    }//GEN-LAST:event_btnConcluirActionPerformed

