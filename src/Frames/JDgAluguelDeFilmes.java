    private void carregaDados(){
        // carregando os valores para  o combo
        
        if(! estatico.Dados.getPessoas().isEmpty()){
                estatico.Dados.getPessoas().forEach((pessoas) ->{
                     if(pessoas instanceof Cliente){
                        cbxCliente.addItem(pessoas);
                     }else{
                        cbxFuncionario.addItem(pessoas);
                     }

                 });

            }
        if(! estatico.Dados.getFilmes().isEmpty()){
            estatico.Dados.getFilmes().forEach((filmes)->{
                cbxFilmes.addItem(filmes);
            });
        }
    }    
    
